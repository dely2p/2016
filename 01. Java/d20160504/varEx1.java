package d20160504;
// 변수? 자료처리를 위한 이름이 부여된 메모리 공간
public class varEx1 {
	public static void main(String[] args){
		// 변수사용
		// 자료형(메모리 공간크기) 변수명(이름)
		byte b1; //b1이라는 이름의 byte크기 변수 선언
		b1 = 10; //b1변수에 10의 값을 대입 [대입연산자(=할당연산자)]
		System.out.println(b1); //b1변수값 출력
		
		b1 = 20; //b1변수에 20의 값을 대입
		System.out.println(b1);
	}//main end
}//class end
